#5. შექმენით ფუნქცია, რომელსაც გადაეცემა სია. თქვენი დავალებაა, რომ ამ სიიდან მოაშოროთ დუპლიკატები (ისეთი ელემენტები, რომლებიც სიაში ერთზე მეტჯერ გვხვდება) და დააბრუნოთ განახლებული სია.
def duplicates(list):
    for i in list:
        x = list.count(i)
        if x > 1:
            list.pop(i)
    return list
print(duplicates([1,1,2,3,4,5,4,5,6,7,8,9]))