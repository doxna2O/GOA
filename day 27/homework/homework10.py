#10. შექმენით ფუნქცია, რომელსაც გადაეცემა კოლექცია. თქვენი დავალებაა, დააბრუნოთ ახალი კოლექცია, სადაც გვექნება ისეთი ელემენტები, რომლებიც საწყის კოლექციაში მარტო ერთჯერ გვხვდება.
def singles(list):
    list1 = []
    for i in list:
        x = list.count(i)
        if x == 1:
            list1.append(i)
    return list1
print(singles([1,1,1,2,3,4]))